# 정수 x 입력받기
x = int(input())

# 앞서 계산된 결과 저장 하기 위한 DP 테이블
DP = [0] * 30001

# 다이나믹 프로그래밍 진행 (보텀업)
for i in range(2, x + 1):
    # 현재의 수에서 1을 빼는 경우
    DP[i] = DP[i - 1] + 1

    # 현재의 수가 2로 나누어 떨어지는 경우
    if i % 2 == 0:
        DP[i] = min(DP[i], DP[i // 2] + 1)

    # 현재의 수가 3로 나누어 떨어지는 경우
    if i % 3 == 0:
        DP[i] = min(DP[i], DP[i // 3] + 1)

    # 현재의 수가 2로 나누어 떨어지는 경우
    if i % 5 == 0:
        DP[i] = min(DP[i], DP[i // 5] + 1)

print(DP[x])